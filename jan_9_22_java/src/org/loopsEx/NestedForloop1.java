package org.loopsEx;

public class NestedForloop1 {

	public static void main(String[] args) {
		System.out.println("** Program Starts");
		for (int i=1; i<=3; i++) 
		{
		   for (int k=1; k<=3; k++)
		   {
			   System.out.println(i);
		   }
		   System.out.println();
		   }
		System.out.println("** Program Ends");
	}

}
