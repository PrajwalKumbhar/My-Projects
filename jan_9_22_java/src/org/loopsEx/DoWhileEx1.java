package org.loopsEx;

public class DoWhileEx1 {

	public static void main(String[] args) {
          System.out.println("**  Programs Starts   **");
          int i=1;
          do {
        	  System.out.println(i);
        	  i++;  
          } while (i>6);
          System.out.println("i = "+i);
          
	       System.out.println("**  Programs Ends  **");
	}
}
