package org.loopsEx;

public class Assigment {

	public static void main(String[] args) {
           for (int i=256; i>=234; i--) {
        	  System.out.println(i);
           }
           System.out.println("************");
           for (int i=-78; i>=-91; i--) {
        	   System.out.println(i);
           }
           System.out.println("************");
           for (char ch = 'q'; ch>='d'; ch--) {
   			System.out.println(ch);
           }
           System.out.println("************");
           for (char ch = 'H'; ch<='W'; ch++) {
   			System.out.println(ch);
           }
	}

}
