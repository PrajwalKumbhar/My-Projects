package org.InterviewQ;

public class EvenOrOddNoEx {

	public static void main(String[] args) {
		System.out.println("Even no form 51 to 70");
           for (int i=51; i<=70; i++) {
        	   if (i%2==0) {
        	   System.out.println(i);
        	   }
           }
           System.out.println("----------------------------");
           
       	System.out.println("Odd no form 51 to 70");
       	
        for (int i=51; i<=70; i++) {
     	   if (i%2!=0) {
     	   System.out.println(i);
     	   }
        }

           
           
        	   
           
        	   
           
        		   
	}

}
