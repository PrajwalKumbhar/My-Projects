package org.narrowingWidening;

public class IntToByte {

	public static void main(String[] args) {
		int i= 130;
		byte b1 = (byte)i;
		System.out.println("int i = "+i);
		System.out.println("byte b1 = "+b1);
		
		int i2 = -130;
		byte b2 = (byte)i2;
		System.out.println("int i2 = "+i2);
		System.out.println("byte b2 = "+b2);



	}

}
 
